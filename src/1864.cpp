#include<bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    char a[]={'L','I','F','E',' ','I','S',' ','N','O','T',' ','A',' ','P','R','O','B','L','E','M',' ','T','O',' ','B','E',' ','S','O','L','V','E','D',' '};
    while(cin>>n)
    {
        for(int i=0;i<n;i++)
        {
            printf("%c",a[i]);
        }
        printf("\n");
    }
    return 0;
}
